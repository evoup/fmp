package com.madhouse.fmp.domain;

public class FmpTemplate {

	
	private Integer id;
	private Integer fmpUserId;
	private String name;
	private String language;
	private Integer ageFrom;
	private Integer ageTo;
	private Integer ageSplit;
	private Integer ageSplitInterval;
	private Integer gender;
	private Integer genderSplit;
	private String location;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getFmpUserId() {
		return fmpUserId;
	}
	public void setFmpUserId(Integer fmpUserId) {
		this.fmpUserId = fmpUserId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLanguage() {
		return language;
	}
	public void setLanguage(String language) {
		this.language = language;
	}
	public Integer getAgeFrom() {
		return ageFrom;
	}
	public void setAgeFrom(Integer ageFrom) {
		this.ageFrom = ageFrom;
	}
	public Integer getAgeTo() {
		return ageTo;
	}
	public void setAgeTo(Integer ageTo) {
		this.ageTo = ageTo;
	}
	public Integer getAgeSplit() {
		return ageSplit;
	}
	public void setAgeSplit(Integer ageSplit) {
		this.ageSplit = ageSplit;
	}
	public Integer getAgeSplitInterval() {
		return ageSplitInterval;
	}
	public void setAgeSplitInterval(Integer ageSplitInterval) {
		this.ageSplitInterval = ageSplitInterval;
	}
	public Integer getGender() {
		return gender;
	}
	public void setGender(Integer gender) {
		this.gender = gender;
	}
	public Integer getGenderSplit() {
		return genderSplit;
	}
	public void setGenderSplit(Integer genderSplit) {
		this.genderSplit = genderSplit;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}

}
